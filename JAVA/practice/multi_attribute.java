public class multi_attribute {
    public static class Main{
    String f_name="Rahul";
    String l_name="Kumar";
    int age=21;
    
    public static void main(String[] args){
        Main obj=new Main();
        System.out.println("Name: "+obj.f_name +" "+obj.l_name);
        System.out.println("Age: "+obj.age);
    }
}
    
}
