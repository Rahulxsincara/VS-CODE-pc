import numpy as np

a = 15
b = 3

add = np.add(a, b)
sub = np.subtract(a, b)
mul = np.multiply(a, b)
div = np.divide(a, b)
mod = np.mod(a, b)
power = np.power(a, b)

print("a =", a)
print("b =", b)
print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)
print("Modulus:", mod)
print("Power:", power)
